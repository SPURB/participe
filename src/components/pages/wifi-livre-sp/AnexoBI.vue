<h4>13. PROCESSOS DE GESTÃO E GERENCIAMENTO DE SERVIÇOS</h4>
<p>13.1. O gerenciamento do ambiente deverá seguir processos e procedimentos definidos entre a SMIT/CCD e a Interessada respeitando os requerimentos específicos de negócio, buscando integridade e transparência na governança. O uso das metodologias, práticas e procedimentos de gestão deverão traduzir-se em um serviço controlado, previsível, estruturado e alinhado às melhores práticas do mercado, além de integrar pessoas, processos, tecnologia e informação.</p>
<p>13.1.1. Recomenda-se que a Interessada disponha de profissionais certificados no uso de metodologias, práticas e procedimentos de gestão de TI, tais como: ITIL e PMI.</p>
<p>13.2. A fiscalização de incidentes será usada pela equipe técnica da PMSP para solicitações de serviço à Interessada, com vistas a restaurar rapidamente a disponibilidade dos serviços, minimizar interrupções e responder às necessidades dos cidadãos. Suas atividades se concentram em monitorar e registrar incidentes, bem como encaminhar as solicitações de serviço. Este processo deverá interagir quando necessário com os processos de gerenciamento de mudanças e gerenciamento da configuração. As seguintes atividades fazem parte da fiscalização de incidentes:
	<br />
a)	Registrar incidentes e solicitações de serviço.
<br />
b)	Categorizar incidentes e solicitações de serviço.
<br />
c)	Priorizar incidentes e solicitações de serviço.
<br />
d)	Isolar incidentes.
<br />
e)	Comunicar incidentes aos níveis superiores de autoridade (dentro do processo ou da hierarquia).
<br />
f)	Acompanhar a evolução de incidentes e das solicitações de serviço.
<br />
g)	Resolver incidentes.
<br />
h)	Encerrar solicitações de serviço.
</p>
<p>13.2.1. O protocolo de fiscalização de incidentes deverá ser elaborado pela Interessada e submetido à aprovação da SMIT/CCD com 10 (dez) dias de antecedência do início da operação da primeira localidade para aprovação. Uma vez aprovado, este deverá ser diligentemente seguido pela Interessada, sob risco de penalidades, conforme item 14 deste Termo de Referência.</p>
<p>13.2.2. Independente do procedimento de fiscalização de incidentes, a Interessada tem responsabilidade de cumprimento de SLA na íntegra.</p>
<p>13.3. A Interessada deverá implementar o gerenciamento de problemas para redução do número de incidentes, abordando as causas raiz, cujas atividades incluem a manutenção preventiva, a análise contínua de tendências e o controle de erros. Este processo está diretamente associado à fiscalização de incidentes e solicitações de serviço, já que alavanca informações sobre incidentes já encerrados e chamados de serviço, bem como aos processos de divulgação de informações aos usuários e à equipe sobre potenciais problemas. As seguintes atividades fazem parte do gerenciamento de problemas:
	<br />
a)	Realizar manutenção preventiva.
<br />
b)	Analisar tendências de incidentes.
<br />
c)	Registrar problemas.
<br />
d)	Identificar causas raiz.
<br />
e)	Acompanhar o progresso da solução de problemas.
<br />
f)	Verificar erros conhecidos.
<br />
g)	Controlar erros conhecidos.
<br />
h)	Resolver problemas.
<br />
i)	Encerrar problemas/erros conhecidos.
</p>
<p>13.3.1. O protocolo de manutenção preventiva e corretiva deverá ser elaborado pela Interessada e submetido à SMIT/CCD para conhecimento, com 10 (dez) dias de antecedência do início da operação da primeira localidade. Esse deverá ser diligentemente seguido pela Interessada.</p>
