<h4>14. PENALIDADES</h4>
<p>14.1. Pela execução do serviço em desacordo com o este Termo de Referência e com as normas legais, a SMIT/CCD poderá, garantida a prévia defesa, aplicar à Interessada as seguintes sanções:</p>
<p>14.1.1. Advertências.</p>
<p>14.1.2. Suspensão temporária de participação em chamamento público e impedimento de celebrar parceria com órgãos e entidades da esfera de governo da administração Pública sancionadora, por prazo não superior a 2 (dois) anos.</p>
<p>14.1.3. Rescisão do instrumento firmado entre as partes e aplicação de declaração de inidoneidade para participar de licitação ou celebrar parcerias com órgãos e entidades de todas as esferas de governo, enquanto perdurarem os motivos determinantes da punição por prazo não superior a 2 (dois) anos.</p>
<p>14.2. Na aplicação da sanção estabelecida no item 14.1.3  será facultada a defesa do interessado no respectivo processo, dirigida ao Secretário de SMIT, no prazo de 10 (dez) dias úteis, contados da abertura de vista, podendo a reabilitação ser requerida após 2 (dois) anos, contados da aplicação da penalidade</p>
<p>14.3. Nas sanções estabelecidas nos itens 14.1.1 e 14.1.2, será facultada a defesa do interessado no respectivo processo, dirigida ao Chefe de Gabinete, nos termos da Portaria SMIT nº 67 de 28 de Agosto de 2018, no prazo de 5 (cinco) dias úteis, contados da abertura de vista. </p>
<p>14.4. O órgão técnico deverá se manifestar sobre a defesa apresentada, em qualquer caso, e a área jurídica quando se tratar de possibilidade de aplicação das sanções previstas nos itens 14.1.2 e 14.1.3.</p>
<p>14.5. A Interessada deverá ser intimada acerca da penalidade aplicada.</p>
<p>14.6. A Interessada terá o prazo de 10 (dez) dias úteis, contado da intimação, para interpor recurso a penalidade aplicada.</p>
<p>14.7. As notificações e intimações de que trata este artigo serão encaminhadas à Interessada, preferencialmente, por meio de correspondência eletrônica, sem prejuízo de outras formas de comunicação, assegurando-se a ciência da Interessada para possibilitar o efetivo exercício do direito ao contraditório e à ampla defesa.</p>
<p>14.8. Os vícios ou falhas na contratação, discriminados abaixo, ensejarão a aplicação de penalidades conforme a pontuação indicada.</p>
<p>14.9. Para fins de aplicação das penalidades previstas na TABELA 3, será considerada a soma das pontuações obtidas nos últimos 12 (doze) meses de prestação do serviço.</p>
<p>14.9.1. Considera-se reincidência quando a Interessada, em nova medição mensal, atingir a pontuação especificada na TABELA 3.</p>
<p>14.10. Na hipótese de aplicação da terceira advertência, a Interessada deverá submeter à SMIT/CCD um Plano de Melhoria visando à correção dos problemas que acarretaram a pontuação obtida, e segui-lo de maneira diligente.</p>

