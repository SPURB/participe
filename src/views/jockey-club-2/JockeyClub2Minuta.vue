					<li>Setor de Transformação: compreende os Subsetores do Hipódromo de Cidade Jardim com áreas funcionalmente dissociadas das atividades do turfe, as quais poderão ser transformadas sem prejuízo do funcionamento do equipamento e das disposições das resoluções de tombamento, contribuindo para o maior aproveitamento do solo urbano na Macroárea de Estruturação Metropolitana e para a diversificação de usos no entorno.</li>
