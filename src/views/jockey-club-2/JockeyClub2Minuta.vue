					<li>garantir a preservação dos atributos paisagísticos e ambientais do Hipódromo de Cidade Jardim, de maneira a manter a unidade do conjunto como patrimônio histórico do Município de São Paulo e como espaço livre de fruição da paisagem, admitindo-se a ampliação dos seus serviços ambientais;</li>
					<li>possibilitar o aumento da integração do Hipódromo de Cidade Jardim e das novas atividades com o entorno e com a cidade, contribuindo para uma maior permeabilidade visual do equipamento.</li>
