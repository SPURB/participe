<p>A revisão dos tempos semafóricos que está sendo realizada promoveu o aumento médio de 22% do tempo disponível ao pedestre, devido não só ao aumento do tempo extra verde como também a maior oferta no número de travessias na hora, consequentemente diminuindo o tempo de espera.</p>
<p>O tempo ideal recomendado para o ciclo semafórico é de 60 a 90 segundos, sendo que o tempo máximo recomendado, quando for impossível se ater a 90 s,é 120 segundos. Afinal, o tempo de espera médio dos pedestres é um preditor significativo do respeito ao semáforo. O resultado será a ampliação do número de oportunidades de travessia, a diminuição do tempo de espera e o conforto, respondendo à inadequação detectada pelo alto número de pessoas idosas entre as vítimas pedestres. Em vias com canteiro central, propõe-se a aplicação de tempo integral para a travessia do pedestre.</p>

<h6>v.	Implementação de projetos de segurança viária em rotas escolares</h6>
<p>Trata-se de ações que têm como objetivo aumentar a segurança viária nos caminhos mais utilizados pelos estudantes nas zonas escolares, com prioridade aos modos de transporte ativo (à pé e de bicicleta). Para atingir tal objetivo, devem ser adotadas medidas como: diminuição da velocidade dos veículos, implantação de elementos de moderação de tráfego, sinalização de locais de travessia, construção de ilhas de refúgio de apoio à travessia, semaforização de cruzamentos, implantação de infraestrutura cicloviária, etc.</p>
<p>O desenvolvimento dos projetos em rotas escolares deverá ser intersecretarial e participativo, incluindo: </p>
<p>●	engajamento da comunidade por meio de oficinas; </p>
<p>●	intervenção intermediária para testar soluções e comunicar a proposta; </p>
<p>●	coleta de dados para avaliação do projeto. </p>
<p>A aplicação dessa metodologia em áreas estratégicas garante a visibilidade da política e consolida parâmetros que devem ser replicados em todas as zonas escolares críticas do município.</p>
<p>Outra ação voltada a áreas escolares são os Territórios Educadores, programa voltado para  crianças da  primeira infância, de 0 a 6 anos, com ênfase em crianças de 0 a 3 anos. </p>
<p>Crianças nesta fase evolutiva sofrem influência do ambiente onde vivem e da interação com pais e cuidadores. Nesta fase da vida, o cérebro da criança produz mais de um trilhão de conexões neuronais, ou sinapses, quando estimuladas através dos sentidos, (conexões cognitivas). A Relação do desenvolvimento cognitivo está diretamente relacionada ao status sócio econômico, crianças oriundas de famílias com menor renda têm menor superfície cortical nas áreas do cérebro responsáveis pela memória, habilidades físico espaciais, linguagem e funções executivas, motivos pelos quais áreas mais pobres da cidade necessitam maior atenção. </p>
<p>A proposta consiste na estruturação de um programa que possa influenciar positivamente no desenvolvimento de crianças nesta faixa etária. Um conjunto de intervenções capazes de induzir e proporcionar estímulos necessários no adequado desenvolvimento das crianças, definindo áreas e rotas no entorno de creches e escolas de primeira infância e promovendo a requalificação de espaços urbanos. Criar estímulos ao longo dos deslocamentos diários, utilizando-se de espaços seguros para o deslocamento e permanência das crianças, criando Trilhas Educadoras, rotas que promovam a interação da criança com estações lúdicas ao longo dos percursos.</p>
 
