<h6>vii.	Expansão e conectividade da rede cicloviária</h6>
<p>O deslocamento por bicicleta gera benefícios à saúde, à economia e ao meio ambiente, tanto ao indivíduo quanto à sociedade. Para incentivar viagens de bicicleta realizadas com segurança, faz-se necessário expandir a rede de infraestrutura cicloviária, melhorar a sua qualidade e aumentar a conectividade. A infraestrutura implantada deve garantir a segurança de ciclistas de todas as idades e habilidades e sua tipologia deve ser adequada às condições operacionais da via, em especial a velocidade e o volume de veículos motorizados. Os parâmetros de projeto para implantação de infraestruturas cicloviárias deverão estar contidos no manual de projetos e obras dos espaços públicos de circulação.</p>
<p>Além da infraestrutura cicloviária, as ações relativas à bicicleta na cidade devem também abarcar:  constante avaliação e monitoramento,  participação social e  fomento à cultura.</p>

<h5>c.	Demais diretrizes de atuação</h5>
<h6>i.	Revisão tamanho de quadra na LPUOS</h6>
<p>O tamanho da quadra tem impacto direto na segurança no trânsito: quanto menores as quadras, maior a segurança. Por isso, quando da próxima revisão da LPUOS, o parâmetro atual de 300 metros deve ser revisto. </p>
<h6>ii.	Revisão do Código de Obras: inclusão do desenho da calçada</h6>
<p>De modo a melhorar a capacidade de análise da PMSP no processo de aprovação dos empreendimentos particulares e evitar conflitos, recomenda-se que, quando da próxima revisão do Código de obras, seja regulamentada a inclusão da obrigação de o particular apresentar na planta de aprovação o desenho da calçada, com as interferências e mobiliários urbanos lindeiros ao lote presentes na via, tais como pontos de parada de ônibus, postes e faixas de travessia.</p>
<h6>iii.	Polos geradores de tráfego</h6>
<p>Frente aos conceitos modernos de mobilidade urbana, define-se como diretriz que as medidas mitigadoras destinem-se a todos os modos de transporte, com prioridade de destinação à mobilidade ativa.</p>
<p>Além disso, traça-se como diretriz a reanálise da legislação sobre o tema para flexibilização do uso das medidas mitigadoras exclusivamente no entorno imediato do empreendimento, quando sua localização é em regiões consolidadas e cujas medidas precisam ser implantadas não imediatamente no entorno do polo gerador.</p>
<h6>iv.	Elaboração do Manual de Projetos e Obras dos Espaços Públicos de Circulação</h6>
<p>Diante da desatualização da Instrução de Projetos IP 003/2004 e da ausência de outras normas municipais que padronizem o desenho de ruas mais seguras e voltadas aos usuários mais vulneráveis, propõe-se a elaboração e publicação de um Manual de Projetos e Obras dos Espaços Públicos de Circulação do Município de São Paulo (ou outro instrumento semelhante, ainda que com outra nomenclatura). O documento terá como referência o Street Desing Manual, elaborado pela cidade de Nova York, e deverá abordar, no mínimo:</p>
<p>●	Breve histórico dos trabalhos anteriores elaborados para o desenho dos espaços públicos, em especial, as calçadas.</p>
<p>●	Diretrizes para projetos de geometria, incluindo calçadas, medidas de moderação de tráfego, ciclovias, corredores de ônibus, etc, tais como parâmetros de largura do leito carroçável, do passeio público, da infraestrutura cicloviária, do canteiro central e de declividade longitudinal para cada tipo de via.</p>
<p>●	Diretrizes para a escolha de materiais a serem utilizados em cada uma das situações.</p>
<p>●	Diretrizes para projetos de iluminação pública.</p>
<p>●	Diretrizes para projetos de arborização e paisagismo dos espaços públicos.</p>
<p>●	Diretrizes para o mobiliário urbano, projetos e implantação.</p>
<p>●	Diretrizes para a drenagem quando relacionada aos espaços públicos.</p>

<h6>v.	Auditoria de Segurança Viária</h6>
<p>A Auditoria de Segurança Viária (ASV) tem por objetivo identificar as deficiências na rede viária previamente à ocorrência de acidentes, através de vistorias técnicas que apontem oportunidades de melhorias nos locais, minimizando a possibilidade de ocorrência dos acidentes e sua gravidade.</p>
<p>A proposta é assumir a ASV como rotina na área de segurança da CET, de maneira a indicar, com base em metodologia objetiva e reconhecida, as melhorias necessárias ao atendimento da segurança do pedestre nas grandes vias da cidade.</p>

<h6>vi.	Uniformização do sistema semafórico</h6>
<p>Institui-se como diretriz a padronização dos semáforos que possuem tempo para pedestre no município de São Paulo para operação com tempo de verde para o pedestre, vermelho piscante para término de travessia e vermelho constante. Apesar de quase a totalidade dos semáforos operarem deste modo, ainda é necessária a adequação de 128 controladores para 100% de padronização. Salienta-se a relevância de realização de campanha para comunicar o padrão para pleno entendimento dos munícipes sobre o funcionamento.</p>

<h6>vii.	Adequação de calçada junto ao recapeamento</h6>
<p>Institui-se como diretriz de atuação a realização dos recapeamentos de vias em conjunto com a adequação das calçadas adjacentes.</p>

<h6>viii.	Aproveitamento do processo de recapeamento para a implantação de mais faixas de travessia de pedestres</h6>
<p>Institui-se como diretriz do processo de recapeamento que a elaboração dos projetos de sinalização horizontal a serem implantados após o recape considere os acidentes de trânsito como elemento essencial para melhoria da sinalização da via. Nesse sentido, propõe-se a massiva expansão da implantação de novas faixas de travessia em meios de quadra e em todas as aproximações dos cruzamentos.</p>

