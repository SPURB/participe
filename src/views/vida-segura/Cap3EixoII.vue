<h5>a.	Objetivos</h5>
<p>●	Aprimorar os parâmetros do marco regulatório de políticas urbanas de São Paulo à luz dos conceitos de Visão Zero e Sistemas Seguros.</p>
<p>●	Incorporar os princípios da Política Nacional de Mobilidade Urbana e Plano Municipal de Mobilidade no planejamento urbano, desenho de ruas e engenharia.</p>
<p>●	Garantir infraestruturas seguras, acessíveis e confortáveis prioritariamente aos usuários mais vulneráveis.</p>

<h5>b.	7 estratégias de atuação prioritárias para intervenções de segurança viária</h5>
<p>Ficam definidos como os sete principais modos de intervenção de segurança viária no município de São Paulo:</p>
<p>●	Implantação de áreas de acalmamento de tráfego</p>
<p>●	Requalificação integrada em corredores de transporte</p>
<p>●	Implementação de projetos de segurança viária em rotas escolares</p>
<p>●	Intervenções de segurança em pontos críticos de travessia do pedestre</p>
