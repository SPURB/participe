<h6>i.	Áreas de acalmamento de tráfego</h6>
<p>A implantação de áreas de acalmamento de tráfego tem por objetivo contribuir para a melhoria da segurança dos pedestres e ciclistas através de um conjunto de medidas em áreas previamente delimitadas. Estas áreas são definidas pela correlação entre concentração de atropelamentos, volume significativo de pedestres, uso do solo comercial/institucional e grande oferta de transportes e serviços. Este é o perfil da maior parte dos subcentros regionais de São Paulo.</p>
<p>A intervenção envolve:</p>
<p>●	fixação da velocidade máxima regulamentada em 30 km/h,</p>
<p>●	reordenamento de fluxos,</p>
<p>●	alterações de geometria das vias,</p>
<p>●	melhoria das calçadas,</p>
<p>●	adequação e implantação de semáforos,</p>
<p>●	instalação de novos equipamentos de fiscalização,</p>
<p>●	instalação de novos equipamentos de segurança, como estreitamentos, lombadas e faixas elevadas,</p>
<p>●	eventuais intervenções que estimulem a apropriação dos espaços públicos pela comunidade local.</p>
<p>A implementação dessas intervenções também é precedida de um amplo debate com a comunidade local. As intervenções pretendidas são discutidas com a comunidade, por meio de audiências públicas, e intervenções provisórias são realizadas.</p>

<h6>ii.	Requalificações integradas em corredores de transportes</h6>
<p>As requalificações integradas em corredores de transporte têm como premissa a integração entre diferentes órgãos, tanto no desenvolvimento dos projetos e implantação das obras (SMT, CET, SPTrans, SMUL, SP Urbanismo, SIURB e SP SPObras), na operação da via (CET, SPTrans, CPTran, GCM) e na integração com ações educacionais e de comunicação. As intervenções incluem:</p>
<p>●	implantação de projetos de sinalização;</p>
<p>●	adequação da geometria da via;</p>
<p>●	adequação de ciclos semafóricos e tempos de travessia;</p>
<p>●	reforço de fiscalização eletrônica</p>
<p>●	sinalização de advertência,</p>
<p>●	reforço de pessoal operacional e ação policial contra alcoolemia.</p>
<p>Tendo em vista a já apontada redução de 68% na mortalidade da Estrada do M’ Boi Mirim, objeto da primeira requalificação integrada, propõe-se a replicação do modelo, que demonstrou efetividade.</p>

<h6>iii.	Intervenções de segurança em pontos críticos de travessia do pedestre</h6>
<p>Visto que a configuração atual da cidade carece de faixas de travessia e de infraestrutura adequada aos pedestres, traça-se como estratégia de atuação a requalificação de pontos críticos de atropelamento, com foco na expansão das faixas de travessia, contemplando as linhas de desejo dos pedestres.</p>
<p>Merece destaque a implantação de travessias em meio de quadra. Foi elaborada nova norma de sinalização das faixas existentes e a serem implantadas em meio de quadra, não semaforizadas, em que consta a inclusão de foco amarelo piscante e iluminação sobre a faixa, proibição de estacionamento 10m antes e depois da faixa de travessia e placa de serviço indicando local de travessia. Compete mencionar que as faixas em meio de quadras não se confundem com faixas recuadas e sua implantação não substitui a necessidade da existência de faixas em todas as pernas dos cruzamentos.</p>

<h6>iv.	Readequação dos tempos semafóricos</h6>
<p>Traça-se como diretriz de atuação a expansão da adequação dos tempos semafóricos para toda a cidade, dando continuidade ao que já está sendo feito em vias com elevado número de atropelamentos, com aumento de tempo de travessia para o pedestre e redução do tempo de ciclo, de forma que o pedestre passa a ter mais oportunidades para a travessia e um maior tempo para efetuá-la.</p>
<p>A CET tem atuado em um conjunto de vias, expandindo o tempo de luz verde para o pedestre, para além dos segundos padrão, mantendo-se o tempo de segurança em vermelho piscante.</p>
