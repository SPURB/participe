<h5>a.	Objetivos</h5>
<p>●	Atuar junto aos órgãos federais para promoção de melhorias na legislação de trânsito que fortaleçam a segurança viária; </p>
<p>●	Fortalecer a gestão e fiscalização de velocidades e demais fatores de risco definidos pela OMS, tendo como ponto focal a segurança e visando reduzir a sensação de impunidade dos motoristas;</p>
<p>●	Fortalecer a fiscalização na priorização de pedestres e ciclistas; </p>
<p>●	Incrementar a sensação de segurança por todos usuários da rua a partir do aumento da percepção geral de fiscalização dos fatores de risco.</p>

<h5>b.	Estratégias e Diretrizes de Atuação</h5>
<h6>i.	Operação e fiscalização com prioridade ao pedestre</h6>
<p>Conforme já aplicado em programas específicos da CET, propõe-se a continuidade e intensificação da priorização ao pedestre nos planos operacionais de fiscalização, com enfoque nos 23 enquadramentos vinculados à segurança do pedestre. Propõe-se a execução do ciclo PDCA sobre as ações do Programa Operacional de Segurança, com a avaliação de impactos e promoção de melhorias na fiscalização para proteção do pedestre. Deve-se implementar a rotineirização da intensificação da operação em pontos críticos de segurança viária. Propõe-se manter e refinar as análises, com foco especial na  concentração e gravidade dos acidentes , para além dos tipos de veículos envolvidos, horários, localização de pólos geradores, pontos de ônibus/estações, declividade e geometria da via, comportamento e outros fatores que podem contribuir para a indicação de ponto operacional voltado à segurança do pedestre.</p>

<h6>ii.	Busca de tecnologia para automação da fiscalização voltada à segurança do pedestre</h6>
<p>Tendo em vista as dificuldades para efetivar parte das autuações que protegem o pedestre, propõe-se que os esforços pela busca de novas tecnologias priorizem o encontro de soluções voltadas à proteção dos pedestres, com vistas a automação da fiscalização, em especial para obediência à prioridade dos pedestres nas faixas de travessia.</p>

<h6>iii.	Fiscalização por velocidade média</h6>
<p>Conforme tratado no diagnóstico, apesar de os equipamentos de fiscalização possuírem condições técnicas para efetivação das autuações pelo controle de velocidade média, sua aplicação depende do governo federal, sendo que as discussões têm se dado em nível federal. Deste modo, propõe-se a intensificação da atuação da PMSP junto ao CONTRAN / DENATRAN e Congresso Nacional para aprovação das mudanças necessárias.</p>

<h6>iv.	Modernização dos serviços do DSV</h6>
<p>Essencial à melhoria da experiência de usuários vulneráveis, como idosos e pessoas com deficiência, a Prefeitura tem como linha de atuação a modernização dos serviços do DSV, por meio do emprego de tecnologia na prestação dos serviços do órgão.</p>
<p>A modernização também visa a melhoria do sistema processual de multas, bem como diminuição de custos do exercício fiscalizatório. Tais melhorias objetivam fortalecer os efeitos benéficos à segurança viária sobre o comportamento dos usuários decorrentes da efetivação da fiscalização.</p>
<p>De modo a eliminar um dos principais gargalos relativos à JARI, propõe-se a implantação de um cadastro de veículos isentos do rodízio municipal para que, por meio da leitura automatizada, estes veículos deixem de ser autuados por desrespeito ao rodízio e a JARI não tenha que analisar os recursos decorrentes.</p>

<h6>v.	Envolvimento dos ônibus do STCUP em acidentes </h6>
<p>Diante do alto número de acidentes com vítimas com o envolvimento de ônibus, da introdução recente de veículos articulados e biarticulados com diferentes espaços de frenagem e diferentes índices de visibilidade e das novas extensões das faixas exclusivas e corredores, a SMT, em conjunto com a CET e SPTrans, irão atuar na análise de medidas específicas para esse segmento. Verifica-se inicialmente a necessidade de se construir um conjunto mais concreto de evidências que subsidiem a tomada de decisão futura. Deste modo, o produto inicial para o biênio 2019-2020 é estudo detalhado dos atropelamentos e colisões envolvendo ônibus urbanos no município. O estudo deve embasar uma rediscussão das velocidades nas faixas exclusivas e corredores e possibilitar a revisão da distribuição da frota conforme a categoria das vias do itinerário.</p>

<h6>vi.	Operação Visibilidade</h6>
<p>Tendo em vista ser a operação do CPTran que mais diretamente se relaciona à proteção dos pedestres, propõe-se sua manutenção, bem como a promoção de maior direcionamento das operações a partir dos resultados de mortalidade do mês anterior.</p>

<h6>vii.	Aprimoramento da regulamentação e fiscalização sobre motocicletas</h6>
<p>Existe atualmente um conjunto de questões legais envolvendo a segurança de motociclistas que vem sendo discutido há anos, principalmente a omissão do CTB quanto à circulação entre faixas adjacentes de veículos e qualidade da habilitação dos motociclistas. Para essas questões, há propostas que estão sendo discutidas no DENATRAN e podem se transformar em lei.</p>
<p>Propõe-se que a PMSP sistematize a participação e o acompanhamento das matérias no CONTRAN / DENATRAN, em especial por meio da participação da CET e DSV nesses órgãos, bem como fomente a discussão na Câmara Temática de motociclistas já proposta.</p>
<p>Propõe-se a gestão junto ao DENATRAN para conduzir e aprovar propostas sobre:</p>
<p>●	Proibir a circulação de motocicletas entre veículos em movimento;</p>
<p>●	Tolerar a circulação entre veículos, com velocidade reduzida (30 km/h), no caso de veículos parados em ambas as faixas adjacentes;</p>
<p>●	Introduzir dois novos enquadramentos de infração: circular entre veículos em movimento e circular entre veículos parados em velocidade incompatível;</p>
<p>●	Regular mecanismos de identificação frontal das motociclistas;</p>
<p>●	Regulamentar equipamento de fiscalização eletrônica para a circulação de motocicletas entre veículos;</p>
<p>●	Aumentar a idade da proibição de crianças em motos: passar de 7 anos para 12 anos;</p>
<p>●	Alteração do art. 280 do CTB, dispensando a necessidade de constar a marca da motocicleta no auto de infração. Texto já previsto no PL 2872/08;</p>
<p>●	Regulamentar os Equipamentos de Proteção Individual do Motociclista, apenas citados no art. 54, mas sem regulamentação no CONTRAN (em razão da ausência de proteção do veículo motocicleta, vestimentas que ofereçam proteção ao condutor e passageiro podem minimizar as consequências de uma ocorrência de trânsito).</p>
<br />
<p>Tendo em vista o recém avanço tecnológico da cobrança de zona azul e a busca pelo estímulo aos modos ativos, verifica-se a possibilidade de se instituir a cobrança de zona azul para motocicletas, mantendo-se a isenção para motociclistas profissionais. São ainda necessários estudos de avaliação de medidas nesse sentido e de seus impactos decorrentes, propostos como produto para o biênio 2019-2020.</p>
<p>Em relação à fiscalização ostensiva sobre as motocicletas, propõe-se a expansão dos dispositivos portáteis de fiscalização. Em comparação aos dispositivos fixos, eles têm maior capacidade de gerar imprevisibilidade e aumento da sensação de fiscalização.  Importante explicitar que o avanço tecnológico pode apontar para novas soluções mais eficazes que os disponíveis atualmente disponíveis. Está em discussão atualmente em âmbito nacional, por exemplo, a possibilidade de as motocicletas terem dispositivos (“chips”), por meio dos quais o controle da velocidade poderia ser exercido, o que tornaria dispensável a fiscalização por meio de equipamentos portáteis de fiscalização.</p>
<p>Propõe-se também a expansão das operações conjuntas sobre motocicletas entre CET e CPTran. Apesar de elas ainda estarem sob período de avaliação, intui-se que será possível comprovar sua eficácia no comportamento dos motociclistas e redução de número de mortes. Deste modo, será feita a verificação dos dados das operações, e, caso verificada a pertinência, sua expansão para o restante da cidade.</p>
<p>Especificamente para as Marginais, verifica-se serem as pistas que mais concentram ocorrências com motociclistas, apesar da proibição de circulação nas pistas expressas da Marginal Tietê e nas centrais desta via no período noturno. Essas proibições foram adotadas exatamente para proteger os motociclistas da exposição ao risco de circular junto de veículos de grande porte, colhendo bons resultados. Deste modo, propõe-se a proibição da circulação de motocicletas na pista expressa da Marginal Pinheiros (sentido Interlagos – Castelo Branco) e a construção de faixa local no sentido Castelo/Interlagos da Marginal Pinheiros para viabilizar a proibição de circulação de motos em pista de maior velocidade.</p>

<h6>viii.	Fiscalização Beber e Dirigir</h6>
<p>O número de choques envolvendo automóveis e sua ocorrência noturna revelam a perda de controle do veículo provavelmente em razão dos efeitos do álcool e drogas sobre o motorista. A Polícia Militar pratica há anos fiscalização nas vias de acesso a regiões boêmias da cidade, o que concorre para a redução de mortes entre motoristas e passageiros. Propõe-se o planejamento de ações conjuntas, mesclando educação de trânsito e fiscalização, bem como parceria com os sindicatos de bares e restaurantes e os fabricantes de bebidas alcoólicas para patrocínio, contratação de profissionais da comunicação e produção de materiais.</p>
<p>Propõe-se submeter maior número de pessoas ao etilômetro, promovendo a expansão da sensação de segurança.</p>

<h6>ix.	Retirada de circulação de veículos conduzidos por condutores contumazes</h6>
<p>À medida que os veículos contumazes representam alto risco aos demais usuários, é necessário intensificar o combate ao tráfego desses veículos. Uma vez retirados de circulação, estes veículos são encaminhados a pátios para que posteriormente possam ser leiloados pela administração pública. Conforme apontado no diagnóstico, a lotação dos pátios é um gargalo desse processo.</p>
<p>Por isso, uma das soluções para efetivar a retirada de circulação dos contumazes é a desvinculação das multas existentes no veículo para leilão. Ou seja, as multas existentes passam a ser cobradas pela Administração Pública do antigo proprietário, mas o veículo é levado a leilão sem autuações, gerando atratividade do leilão e promovendo a liberação dos pátios.  </p>

<h6>x.	Melhorias relativas aos equipamentos de fiscalização eletrônica </h6>
<p>Para os equipamentos de fiscalização eletrônica, são traçadas as seguintes estratégias:</p>
<ul>
	<li>Expansão do número de pontos de fiscalização, por meio de equipamentos de fiscalização:
		<ul>
			<li>Preferencialmente não intrusivos, mas com expansão também dos não intrusivos;</li>
			<li>Barreiras eletrônicas, preferencialmente não intrusivas</li>
		</ul>
	</li>
	<li>Expansão dos equipamentos portáteis de fiscalização</li>
	<li>Inclusão de display de velocidades em pontos de fiscalização estratégicos.</li>
	<li>Instituição de equipamento de verificação de peso dos veículos.</li>
	<li>Implantação de mecanismo que permita o alerta imediato de veículos irregulares</li>
	<li>Melhoria da gestão dos equipamentos por meio de Central de Controle.</li>
</ul>

<h6>xi.	Fiscalização remota</h6>
<p>A fiscalização remota é aquela realizada à distância, através de agentes de trânsito operando por videomonitoramento. Essa modalidade de fiscalização remota ainda não é praticada na capital paulista mas se encontra em fase de estudos, sendo ainda necessários ajustes nas normas e nos equipamentos de fiscalização para que possa ser efetivada com segurança jurídica. A grande vantagem dessa modalidade é a eficiência gerada na fiscalização de pontos críticos e cujas infrações não são facilmente percebidas por mecanismos automatizados, garantindo maior eficiência à atuação do agente de trânsito.</p>

<h6>xii.	Central de controle única</h6>
<p>Propõe-se a atuação da PMSP para unificação das centrais de controle dos diferentes órgãos que monitoram as vias, bem como o uso de aplicativo para dispositivo móvel que permita ao usuário fazer a notificação georreferenciada de acidentes de trânsito e que automaticamente acione todos os órgãos necessários.</p>

<h6>xiii.	Aprimoramento da regulamentação e fiscalização sobre o transporte de cargas</h6>
<p>De modo a promover a gestão de risco relativa ao transporte de cargas no município e aumentar o controle e a previsibilidade sobre os acidentes de trânsito envolvendo caminhões, propõe-se a instituição de regulamentação que trace as rotas permitidas para o transporte de carga excedente e perigosas.</p>
<p>Além disso, para além das imposições de idade máxima já existentes para cadastramento e efetivação da autorização de acesso aos locais restritos dos caminhões do tipo VUC, concretagem-bomba e concretagem, propõe-se a expansão destas regulamentações para outros tipos de caminhões.</p>
<p>Propõe-se também a intensificação da fiscalização sobre a adequabilidade da carga carregada frente à autorização dada e a sobre produtos perigosos para redução do número de irregularidades verificadas e manutenção do baixo número de ocorrências.</p>
<p>Para diminuição dos acidentes decorrentes das alturas excedentes dos caminhões, propõe-se a atuação da PMSP para implantação de equipamentos de fiscalização do tipo pórtico nas entradas das rodovias, com o intuito de barrar a entrada em São Paulo de caminhões que excedam as alturas limites. Recomenda-se que estes dispositivos leiam informações complementares do veículo e da carga.</p>

