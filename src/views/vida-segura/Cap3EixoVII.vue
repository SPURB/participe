<h6>iii.	Temas prioritários</h6>
<p>De modo a concentrar o uso dos recursos financeiros e de pessoal, propõe-se cinco temas prioritários para as ações educacionais e de comunicação, tratadas abaixo. Explicita-se, como ponto em comum entre eles, o foco nos fatores de risco da OMS que diretamente afetam a segurança dos usuários mais vulneráveis: excesso de velocidade, beber e dirigir e condução distraída (em especial relacionada ao uso do celular).</p>
<p>1.	Proteção ao pedestre</p>
<p>Tendo em vista serem as principais vítimas do trânsito de São Paulo, define-se como prioridade principal das ações de educação e comunicação da PMSP a proteção ao pedestre.</p>
<p>Destaca-se que, apesar de o conteúdo das mensagens tratar da proteção ao pedestre, propõe-se que elas sejam destinadas prioritariamente aos condutores de veículos, uma vez que são eles os agentes causadores dos atropelamentos (ainda que não sejam necessariamente os “culpados”). Tal diretriz se apoia no princípio de responsabilização do menos vulnerável para o mais vulnerável imposto pela Política Nacional de Mobilidade e pelo PlanMob. Ainda, parte-se do entendimento de que a infraestrutura da cidade foi, a partir do século XX, desenvolvida voltada aos carros e não aos pedestres, de modo que a estes ainda não é ofertada a infraestrutura adequada para seus deslocamento. Por fim, o Art. 69 do CTB estabelece que não havendo faixas de pedestres a 50 metros de onde pessoa se encontra, ele poderá atravessar onde for mais conveniente de forma perpendicular;</p>
	<p>Mensagens principais:</p>
<p>●	Os pedestres são os usuários que mais morrem em São Paulo;</p>
<p>●	Atenção aos pedestres idosos, que são os pedestres que mais morrem em São Paulo;</p>
<p>●	Atenção às crianças e às zonas escolares. As crianças que mais morrem no trânsito de São Paulo são as pedestres, não as de dentro dos veículos.</p>
<p>●	Respeito à faixa de pedestre;</p>
<p>●	Respeito ao pedestre, mesmo fora da faixa;</p>
<p>●	Explicação sobre os tempos semafóricos e o “vermelho piscante”.</p>
<p>2.	Comportamento do motociclistas jovens</p>
<p>Em 2017, os motociclistas foram os usuários com maior número de vítimas no trânsito de São Paulo, representando 49,7% do total, e corresponderam a 39% das vítimas fatais. Entre os motociclistas mortos no trânsito, 60% têm até 29 anos e 92% são do gênero masculino, motivo pelo qual o público motociclista jovem do gênero masculino deve ser prioritário nas ações educacionais e de comunicação.</p>
<p>Mensagens principais:</p>
<p>●	Atenção aos pontos cegos dos caminhões e ônibus;</p>
<p>●	Atenção aos momentos em que os automóveis  estão congestionados;</p>
<p>●	Riscos do uso do celular enquanto dirige;</p>
<p>●	Riscos do uso de álcool e drogas e a pilotagem;</p>
<p>●	Riscos do excesso de velocidade.</p>
<p>3.	Compartilhamento da pista de rolamento</p>
<p>Tendo em vista os efeitos positivos para a saúde pública e para o meio ambiente, além de sua eficiência como meio de transporte urbano, a bicicleta deve definitivamente ganhar relevância como modo de transporte.	</p>
<p>No contexto de incentivo à expansão do uso da bicicleta como modo de transporte, acentuado pelas bicicletas compartilhadas, verifica-se o risco do aumento do número de mortes de ciclistas. Este cenário aponta a necessidade de consolidar a  visão de que os ciclistas têm direito à utilizar a rua, tanto em espaços exclusivos (ciclovias e ciclofaixas), quanto em espaços compartilhados com demais veículos. </p>
<p>Além do ciclista, propõe-se foco na relação entre os motociclistas e os demais usuários, tendo em vista os conflitos existentes e a alta mortalidade de motociclistas.</p>
	<p>Mensagens principais:</p>
<p>●	A pista de rolamento não se destina apenas aos automóveis, mas também às motocicletas e bicicletas;</p>
<p>●	Divulgação das regras do CTB referentes às bicicletas e às motocicletas, e sua relação com os demais veículos;</p>
<p>●	Benefícios de se pedalar (poluição atmosférica, saúde, etc)</p>
<p>Ainda no que se refere às bicicletas, tendo em vista a expressiva ocorrência de acidentes no período noturno, verifica-se a relevância de promover a conscientização aos ciclistas da importância do uso de elementos de visibilidade noturna do veículo, como luzes e refletivos.</p>
<p>4.	Condutor de Ônibus</p>
<p>À medida que os ônibus estão envolvidos em 20% dos atropelamentos fatais, entende-se como prioridade ações internas direcionadas aos condutores de ônibus. Incluem-se como destinatários os motoristas dos ônibus do STCUP, gerido pela SPTrans, do transporte intermunicipal, gerido pela EMTU, e os dos fretados.</p>
	<p>Mensagens principais:</p>
<p>●	Atenção à ampliação dos riscos de atropelamento quando as faixas de rolamento dos veículos estão congestionadas e a de ônibus não;</p>
<p>●	Conscientização dos pontos críticos de atropelamento com envolvimento de ônibus;</p>
<p>●	Atenção aos pontos cegos.</p>
<p>5.	Divulgação de dados</p>
<p>Por fim, a última prioridade definida neste plano é a própria divulgação das informações mais relevantes a respeito da segurança viária. Verifica-se que, de modo geral, a sociedade tem pouco conhecimento dos dados e da gravidade do problema. Diversos tipos de informações e recortes sobre os acidentes devem ser continuamente divulgadas, com o objetivo de que a pauta permaneça constantemente na agenda pública. Pode-se divulgar, por exemplo, que as mortes decorrentes de trânsito são a segunda maior causa de óbito de jovens (entre 15 e 29 anos) em São Paulo, que os pedestres são as principais vítimas, as vias em que há mais vítimas em cada região, etc.  </p>

<h6>iv.	Modos de atuação</h6>
<p>1.	Ações em campo integradas</p>
<p>Para elevar a probabilidade de os projetos de segurança viária serem efetivos, é necessário que sejam antecedidos de ações de mobilização da opinião pública e do público alvo. Como linha de atuação primordial deste eixo, propõe-se a expansão do modelo aplicado no projeto piloto de promoção da segurança viária em rotas escolares realizado na região de Itaquera. Neste projeto, antes da implantação da sinalização planejada, foram realizadas reuniões com a diretoria das escolas da região, elaborado material de divulgação, efetuada uma oficina com escolares e comunidade e aplicada uma sinalização provisória no solo, com cal colorida, de maneira a testar as novas configurações viárias. No caso, a comunicação e mobilização da comunidade foram lideradas pelas equipes de educação de trânsito da CET, totalmente integradas ao espírito do projeto e nas discussões de concepção da nova sinalização. Este modelo de atuação eleva a probabilidade de sucesso e obediência às regras de circulação, uma vez que os usuários participam das discussões sobre as soluções e as características dos projetos, vivenciam o teste da nova sinalização e opinam sobre ela.</p>
<p>2.	Campanhas gerais</p>
<p>Conforme verificado no diagnóstico, a PMSP não possui histórico de campanhas continuadas voltadas à redução de mortes decorrentes do trânsito. Deste modo, traça-se como diretriz a continuidade de campanhas em larga escala, para que a consolidação de novo paradigma voltado à segurança viária possa ser alcançado. As campanhas de mídia de massa bem executadas devem seguir as melhores práticas internacionais e ser baseadas em evidências em todas as etapas do seu ciclo: identificação do fator e do comportamento de risco, definição de objetivos mensuráveis e alcançáveis, definição do público-alvo e escolha dos meios de comunicação de acordo com os hábitos do público-alvo. Uma campanha eficaz se concentra em um fator de risco específico e se centra nos canais de mídia que atingem a maior audiência com o público-alvo (o canal principal frequentemente é a TV, seguida de rádio e mídia exterior). É essencial garantir níveis adequados de exposição (abrangência e frequência) da campanha ao público-alvo. </p>
<p>Além disso, é fundamental fazer uso de mecanismos de pesquisas quantitativa e qualitativa para testar as mensagens e conceitos da campanha junto ao público-alvo, com vistas a identificar mensagens poderosas que melhor ressoem e levem a mudanças de comportamento, e, na avaliação de campanha, com o objetivo de medir sua memorização pelo público e a evolução dos conhecimentos e atitudes, indicando em que medida os objetivos foram alcançados. É útil, ainda, complementar essas descobertas com outras fontes de dados relevantes, incluindo números sobre acidentes, dados observacionais e policiais, a fim de correlacionar a eficácia das campanhas e a implementação.</p>
<p>3.	Cuidado com as calçadas</p>
<p>Tendo vista que mais de 80% das calçadas do município são responsabilidade dos proprietários, a Prefeitura irá atuar em campanhas que tratem da importância de a cidade ter calçadas acessíveis e que estimulem a mobilidade a pé, de cada proprietário cuidar e manter sua calçada dentro dos padrões que estão sendo estabelecidos.</p>
<p>4.	Comunicação Interna</p>
<p>Considerando que a mudança de paradigma apontada deve ocorrer também dentro dos órgãos públicos, para além das capacitações propostas neste eixo, é necessária a continuidade de ações de comunicação sobre segurança viária voltadas ao público interno, para que os novos conceitos de Visão Zero e Sistemas Seguros sejam consolidados nas práticas cotidianas do poder público.</p>
<p>5.	Plataforma digital de segurança viária </p>
<p>Propõe-se como frente de atuação da PMSP a criação de plataforma digital sobre segurança viária, de grande alcance e impacto. Objetiva-se que o espaço seja referência nacional sobre o tema e agregue publicações não apenas da PMSP, mas também dos demais stakeholders relacionados ao tema.</p>
<p>6.	Utilização de mídias públicas</p>
<p>Na abordagem de que a Segurança Viária é problema de saúde pública e não apenas da pauta de transportes, propõe-se a instituição de parcerias entre a CET e a SMT com outras secretarias e órgãos, tais como Secretarias de Educação, Saúde, Metrô, CPTM, etc. para busca de espaços para divulgação de conteúdo voltado à segurança viária.</p>
<p>7.	CETET</p>
<p>No âmbito específico de atuação do CETET, todo seu acervo de atividades será disponibilizado e direcionado ao fortalecimento da Visão Zero e Sistemas Seguros, quais sejam:</p>
<p>●	 trabalho realizado em suas dependências localizadas na Barra Funda; </p>
<p>●	no Espaço Vivencial de Trânsito Caio Graco da Silva Prado (anexo ao CETET) e no CEMOB Leste (Centro de Pesquisa e Educação em Mobilidade Urbana);</p>
<p>●	o atendimento em instituições como empresas e escolas;</p>
<p>●	as ações diretamente realizadas no espaço urbano viário,</p>
<p>●	as ações na internet (uso de rede social, blog, Portal Interativo e cursos em EaD);</p>
<p>●	as ações com mímicos;</p>
<p>●	a realização  anual do Prêmio CET de Educação.</p>
<p>Adicionalmente, institui-se como diretriz a intensificação do treinamento e capacitação de trânsito para motociclistas em geral e para motofretistas. Propõe-se ampliar as atividades de educação para motociclistas desenvolvidas pela CET, como cursos de Pilotagem Segura para motociclistas e Curso de Formação de Motofretistas. Assim, novos espaços e o reforço dos recursos destinados ao aperfeiçoamento dos motociclistas para a condução segura poderão facilitar o acesso a um maior número de condutores e minimizar os efeitos da formação insuficiente que ocorre no processo de habilitação.</p>
<p>Adicionalmente, traça-se como diretriz a expansão da realização da capacitação de multiplicadores para a disseminação de conteúdos de segurança viária e mobilidade urbana em outras unidades públicas não pertencentes ao CETET, tais como unidades de educação e saúde.</p>
<p>8.	Entendimento dos perfis de motociclistas</p>
<p>Institui-se como estratégia de atuação ações para o melhor conhecimento dos universos existentes relativos aos motociclistas e motofretistas que circulam na cidade de São Paulo. Intui-se que nos últimos anos o público mudou significativamente e, para tornar a comunicação eficaz, é necessário identificar o perfil, características e motivações destes usuários.</p>
<p>9.	Termo de Cooperação CET-SMTE</p>
<p>Com o objetivo de aumentar a visibilidade em cruzamentos mais críticos do município, a PMSP elaborou uma ação de orientação de travessia em faixa de pedestre. Esta atividade deve ser realizada nos cruzamentos que apresentam altos índices de atropelamento e/ou locais com grande volume de pedestres. O objetivo da campanha é aumentar a atenção aos pedestres em cruzamentos e influenciar comportamentos mais seguros por todos os usuários da via.</p>
<p>A ação se viabiliza por meio do Programa Operação Trabalho, em que pessoas em situação de alta vulnerabilidade socioeconômica são capacitadas e atuam como orientadores de travessia.</p>
<p>10.	Fortalecimento dos CONSEGs como canal multiplicador de informações</p>
<p>Conforme descrito no diagnóstico, os Conselhos Comunitários de Segurança são um importante canal de interlocução entre poder público e população local. Entende-se que o espaço deve ser melhor explorado para tratar do tema da segurança viária, dotando as populações locais de mais informações sobre o panorama da segurança viária em sua região.</p>
<p>Adicionalmente aos CONSEGs, outro canal a ser explorado para divulgação de informações relativas à segurança viária são das chamadas vizinhanças solidárias, grupos digitais dos bairros com a presença das lideranças locais e da polícia militar.</p>
	
<h6>v.	Capacitação</h6>
<p>1.	Programa de Capacitação interna</p>
<p>Propõe-se a criação de programa específico de capacitação voltada aos servidores públicos da PMSP e outros órgãos, e ao mesmo tempo aberto ao público, para tratar dos novos conceitos de mobilidade urbana, incluindo visão zero e sistemas seguros. Sugere-se o oferecimento dos cursos continuamente em estruturas existentes de formação de pessoal voltados a servidores públicos, tais como a Escola Municipal de Administração Pública.</p>
<p>O objetivo é que todos os servidores da PMSP, em especial aqueles que se relacionam com o sistema viário, seja pela atuação em campo, seja pela elaboração de projetos e implementação de obras, compreendam os conceitos de visão zero e sistemas seguros e vejam a segurança viária como elemento prioritário em sua atuação</p>
<p>2.	Disciplina Ruas Completas</p>
<p>Propõe-se a atuação da PMSP junto a universidades para fomento à criação de disciplinas de graduação em cursos de arquitetura e urbanismo chamadas “ruas completas” ou outro nome congênere, que trate do tema.</p>
<p>3.	Capacitação de motoristas do STCUP</p>
<p>Propõe-se o desenvolvimento de programa de capacitação de motoristas, baseado em dois vetores de atendimento: segurança e acolhimento; a  partir da reavaliação das ações voltadas ao atendimento praticado pelas empresas operadoras, identificando os aspectos que possam não estar sendo atendidos e aqueles que devem ser melhorados.  </p>
<p>As ações devem ser viabilizadas por meio do contato permanente entre a SPTrans e as empresas operadoras. Reuniões mensais em que a área de treinamento e desenvolvimento da SPTrans se reúne com os RHs das empresas, seus respectivos coordenadores de recrutamento e seleção e de treinamento e desenvolvimento e instrutores de treinamentos, convidando representantes da sociedade civil, a fim de que estes apresentem suas demandas para incremento no atendimento e na segurança no desempenho do transporte.</p>
<p>Além disso, os RHs das empresas são instadas a desenvolverem suas atividades de capacitação de acordo com as diretrizes da SPTrans da seguinte forma:</p>
<p>●	avaliar o que é desenvolvido para qualificação de operadores, notadamente na questão do atendimento e segurança, e possibilitar a troca de experiências sistematicamente;</p>
<p>●	definir calendário mensal de acompanhamento com os profissionais das operadoras para discussões técnicas, a fim de minimizar dificuldades e dirimir dúvidas;</p>
<p>●	avaliar cada um dos programas existentes à luz de um programa mínimo, estabelecendo prazos para as melhorias necessárias ou a implantação do programa para as empresas que não o tiverem instituído;</p>
<p>●	estabelecer rotina de reciclagem para motoristas, em que esses profissionais sejam integrados à avaliação das questões locais (reclamações de sua empresa) e passem a se comprometer com a solução, carga horária mínima de 8 horas;</p>
<p>●	trazer os representantes dos diversos segmentos da sociedade civil presentes na vida da cidade, em especial na vida do transporte coletivo por ônibus, de forma que suas demandas sejam conhecidas e ao mesmo tempo conheçam o que é feito.</p>
<p>4.	Gestão da capacitação dos motoristas do STCUP</p>
<p>A principal melhoria na gestão das capacitações dos motoristas do STCUP é a implantação de auditorias permanentes, via sistema informatizado e presenciais.  A SPTrans está desenvolvendo um sistema informatizado que possibilitará o acompanhamento das atividades de capacitação das empresas, de maneira a cobrar a realização do planejado, assim como verificar se as ações e a carga horária estão de acordo com o conteúdo esperado. Além disso, visitas de auditorias serão realizadas para constatar se o cronograma previsto está sendo efetivado e analisar a qualidade desta realização. Em caso descumprimento, a SPTrans poderá aplicar penalidades contratuais às operadoras.</p>
<p>Além disso, prevê-se ampliar o cadastro de profissionais treinados, implantar cadastro geral de profissionais de RH com responsabilidade pela qualificação dos profissionais em cada empresa; e ampliar o cadastro de profissionais treinados e autorizados a operar no sistema, compartilhado com o cadastro operacional existente.</p>
<p>Por fim, propõe-se a criação e manutenção de cadastro de instituições que possam aplicar a programação mínima de treinamentos estabelecida pela SPTrans.</p>

