<p>●	Reforçar a responsabilidade compartilhada - social e política - pela segurança viária, ao invés da responsabilização individual;</p>
<p>●	Reforçar a importância das políticas públicas voltadas à segurança viária, buscando a mudança de comportamento da sociedade.</p>
<p>●	Ampliar o conhecimento da sociedade sobre os problemas relacionados à segurança viária, bem como suas causas e consequências;</p>
<p>●	Dar voz ativa à sociedade, por meio de trabalho em rede com os diversos segmentos sociais, com o objetivo de que a defesa da segurança viária seja promovida por todos, não só pelo poder público;</p>
<p>●	Fortalecer os mecanismos de accontabillity pública.</p>

