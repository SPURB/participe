<h5>a.	Objetivos</h5>
<p>●	Alterar o paradigma da cultura sobre mobilidade urbana no Município de São Paulo, da visão tradicional voltada à priorização do fluxo, ao veículo automotor individual e à ênfase no comportamento da vítima para uma visão de assunção de responsabilidade pelas mortes e lesões graves no trânsito voltada à segurança viária e à proteção dos cidadãos, especialmente os usuários mais vulneráveis;</p>
<p>●	Difundir a compreensão das multicausalidades associadas aos acidentes de trânsito e consolidar a percepção de evitabilidade de todos acidentes com mortos e feridos graves no trânsito;</p>
<p>●	Institucionalizar a utilização da comunicação de massa como importante instrumento de mudança comportamental no trânsito;</p>
<p>●	Incentivar e fomentar a cultura da mobilidade ativa;</p>
<p>●	Consolidar os princípios de Visão Zero e Sistemas Seguros nas práticas cotidianas relativas à mobilidade urbana no Município de São Paulo.</p>

<h5>b.	Estratégias e Diretrizes de atuação</h5>
